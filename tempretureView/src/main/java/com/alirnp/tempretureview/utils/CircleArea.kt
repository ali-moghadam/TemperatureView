package com.alirnp.tempretureview.utils

class CircleArea {
    private var xStart = 0f
    private var xEnd = 0f

    private var yStart = 0f
    private var yEnd = 0f

    fun getXStart(): Float {
        return xStart
    }

    fun setXStart(xStart: Float) {
        this.xStart = xStart
    }

    fun getXEnd(): Float {
        return xEnd
    }

    fun setXEnd(xEnd: Float) {
        this.xEnd = xEnd
    }

    fun getYStart(): Float {
        return yStart
    }

    fun setYStart(yStart: Float) {
        this.yStart = yStart
    }

    fun getYEnd(): Float {
        return yEnd
    }

    fun setYEnd(yEnd: Float) {
        this.yEnd = yEnd
    }
}